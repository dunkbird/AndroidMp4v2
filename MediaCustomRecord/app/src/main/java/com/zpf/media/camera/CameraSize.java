package com.zpf.media.camera;

/**
 * author:zpf
 * date:2019-08-16
 */
public class CameraSize {

    private final int width;
    private final int height;

    public CameraSize(int width, int height) {
        this.width = width;
        this.height = height;
    }

    public int getWidth() {
        return width;
    }

    public int getHeight() {
        return height;
    }
}
